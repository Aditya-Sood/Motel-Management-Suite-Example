    private void BtnAddRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddRoomActionPerformed
        String room_no, room_cat="", cat="";
        int charges, qty, p=1 ;
        
        qty = Integer.parseInt(TxtFdNoOfRoom.getText());
        
        if(RbAddSlv.isSelected())
        {
            room_cat = "SL0";
            cat = "Silver";
        }
        
        else if(RbAddGld.isSelected())
        {
            room_cat = "GD0";
            cat = "Gold";
        }
        
        else if(RbAddPlt.isSelected())
        {
            room_cat = "PT0";
            cat = "Platinum";
        }
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/aditya_sud?user="+"root"+"&password="+"MYSQL");
            
            Statement stmt = con.createStatement();
            
            for(; p<=qty ; p++)
            {
                String Q1 ="Select * from Room where Class='" + cat +"'" ;
                ResultSet rs1 = stmt.executeQuery(Q1);
                rs1.next();
                charges = rs1.getInt("Charges");
                rs1.last();
                int i = rs1.getRow();
                room_no = room_cat +(i+1);
            
                String Q2 ="Insert into Room values('" + room_no + "','" + cat + "','N'," + charges + ")";
                stmt.executeUpdate(Q2);
            }
            
            JOptionPane.showMessageDialog(this, "" + (p-1) + " rooms of " + cat + " Class have been added.");
            
            }
        
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        
    }//GEN-LAST:event_BtnAddRoomActionPerformed

